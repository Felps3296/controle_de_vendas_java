    public void listar(){
        FuncionariosDAO dao = new FuncionariosDAO();
        //lista vai receber a lista de clientes
                              //Método listar clientes
        List<Funcionarios> lista = dao.listarFuncionarios();        
        /*os dados que são exibidos na tabela, geralmente eles devem
        estar dentro de um objeto do tipo DefaulTableModel(O Conteúdo da tabela).
        Criando o Objeto que vai armazenar os dados dessa lista para mostrar na 
        tabela de clientes.
        */
        DefaultTableModel dados = (DefaultTableModel) tabelaFuncionarios.getModel();
        //limpar os dados
        dados.setNumRows(0);
        
        //Para cada linha vou adicionar um novo vetor de objetos
        for (Funcionarios c: lista){
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getRg(),
                c.getCpf(),
                c.getEmail(),
                c.getSenha(),
                c.getCargo(),
                c.getNivel_acesso(),
                c.getTelefone(),
                c.getCelular(),
                c.getCep(),
                c.getEndereco(),
                c.getNumero(),
                c.getComplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getEstado(),
            });
        }
    }
